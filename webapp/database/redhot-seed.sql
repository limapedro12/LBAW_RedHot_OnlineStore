  first_name VARCHAR(256),
  last_name VARCHAR(256),
  email VARCHAR(256),
  telefone VARCHAR(256),
  morada VARCHAR(256),
  porta VARCHAR(256),
  andar VARCHAR(256),
  codigo_postal VARCHAR(256),
  localidade VARCHAR(256),
  pais VARCHAR(256),
  nif VARCHAR(256),
  metodo_pagamento VARCHAR(256)
